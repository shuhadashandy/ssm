	// Validate form
	protected function validateForm()
	{
		global $Language, $FormError;

		// Initialize form error message
		$FormError = "";

		// Check if validation required
		if (!SERVER_VALIDATE)
			return ($FormError == "");
		if ($this->ID->Required) {
			if (!$this->ID->IsDetailKey && $this->ID->FormValue != NULL && $this->ID->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->ID->caption(), $this->ID->RequiredErrorMessage));
			}
		}
		if ($this->Judul->Required) {
			if (!$this->Judul->IsDetailKey && $this->Judul->FormValue != NULL && $this->Judul->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->Judul->caption(), $this->Judul->RequiredErrorMessage));
			}
		}
		if ($this->Gambar->Required) {
			if ($this->Gambar->Upload->FileName == "" && !$this->Gambar->Upload->KeepFile) {
				AddMessage($FormError, str_replace("%s", $this->Gambar->caption(), $this->Gambar->RequiredErrorMessage));
			}
		}
		if ($this->Isi->Required) {
			if (!$this->Isi->IsDetailKey && $this->Isi->FormValue != NULL && $this->Isi->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->Isi->caption(), $this->Isi->RequiredErrorMessage));
			}
		}

		// Return validate result
		$validateForm = ($FormError == "");

		// Call Form_CustomValidate event
		$formCustomError = "";
		$validateForm = $validateForm && $this->Form_CustomValidate($formCustomError);
		if ($formCustomError <> "") {
			AddMessage($FormError, $formCustomError);
		}
		return $validateForm;
	}

	// Update record based on key values
	protected function editRow()
	{
		global $Security, $Language;
		$filter = $this->getRecordFilter();
		$filter = $this->applyUserIDFilters($filter);
		$conn = &$this->getConnection();
		$this->CurrentFilter = $filter;
		$sql = $this->getCurrentSql();
		$conn->raiseErrorFn = $GLOBALS["ERROR_FUNC"];
		$rs = $conn->execute($sql);
		$conn->raiseErrorFn = '';
		if ($rs === FALSE)
			return FALSE;
		if ($rs->EOF) {
			$this->setFailureMessage($Language->phrase("NoRecord")); // Set no record message
			$editRow = FALSE; // Update Failed
		} else {

			// Save old values
			$rsold = &$rs->fields;
			$this->loadDbValues($rsold);
			$rsnew = [];

			// Judul
			$this->Judul->setDbValueDef($rsnew, $this->Judul->CurrentValue, "", $this->Judul->ReadOnly);

			// Gambar
			if ($this->Gambar->Visible && !$this->Gambar->ReadOnly && !$this->Gambar->Upload->KeepFile) {
				$this->Gambar->Upload->DbValue = $rsold['Gambar']; // Get original value
				if ($this->Gambar->Upload->FileName == "") {
					$rsnew['Gambar'] = NULL;
				} else {
					$rsnew['Gambar'] = $this->Gambar->Upload->FileName;
				}
			}

			// Isi
			$this->Isi->setDbValueDef($rsnew, $this->Isi->CurrentValue, "", $this->Isi->ReadOnly);
			if ($this->Gambar->Visible && !$this->Gambar->Upload->KeepFile) {
				$oldFiles = EmptyValue($this->Gambar->Upload->DbValue) ? array() : array($this->Gambar->Upload->DbValue);
				if (!EmptyValue($this->Gambar->Upload->FileName)) {
					$newFiles = array($this->Gambar->Upload->FileName);
					$NewFileCount = count($newFiles);
					for ($i = 0; $i < $NewFileCount; $i++) {
						if ($newFiles[$i] <> "") {
							$file = $newFiles[$i];
							if (file_exists(UploadTempPath($this->Gambar, $this->Gambar->Upload->Index) . $file)) {
								if (DELETE_UPLOADED_FILES) {
									$oldFileFound = FALSE;
									$oldFileCount = count($oldFiles);
									for ($j = 0; $j < $oldFileCount; $j++) {
										$oldFile = $oldFiles[$j];
										if ($oldFile == $file) { // Old file found, no need to delete anymore
											unset($oldFiles[$j]);
											$oldFileFound = TRUE;
											break;
										}
									}
									if ($oldFileFound) // No need to check if file exists further
										continue;
								}
								$file1 = UniqueFilename($this->Gambar->physicalUploadPath(), $file); // Get new file name
								if ($file1 <> $file) { // Rename temp file
									while (file_exists(UploadTempPath($this->Gambar, $this->Gambar->Upload->Index) . $file1) || file_exists($this->Gambar->physicalUploadPath() . $file1)) // Make sure no file name clash
										$file1 = UniqueFilename($this->Gambar->physicalUploadPath(), $file1, TRUE); // Use indexed name
									rename(UploadTempPath($this->Gambar, $this->Gambar->Upload->Index) . $file, UploadTempPath($this->Gambar, $this->Gambar->Upload->Index) . $file1);
									$newFiles[$i] = $file1;
								}
							}
						}
					}
					$this->Gambar->Upload->DbValue = empty($oldFiles) ? "" : implode(MULTIPLE_UPLOAD_SEPARATOR, $oldFiles);
					$this->Gambar->Upload->FileName = implode(MULTIPLE_UPLOAD_SEPARATOR, $newFiles);
					$this->Gambar->setDbValueDef($rsnew, $this->Gambar->Upload->FileName, "", $this->Gambar->ReadOnly);
				}
			}

			// Call Row Updating event
			$updateRow = $this->Row_Updating($rsold, $rsnew);
			if ($updateRow) {
				$conn->raiseErrorFn = $GLOBALS["ERROR_FUNC"];
				if (count($rsnew) > 0)
					$editRow = $this->update($rsnew, "", $rsold);
				else
					$editRow = TRUE; // No field to update
				$conn->raiseErrorFn = '';
				if ($editRow) {
					if ($this->Gambar->Visible && !$this->Gambar->Upload->KeepFile) {
						$oldFiles = EmptyValue($this->Gambar->Upload->DbValue) ? array() : array($this->Gambar->Upload->DbValue);
						if (!EmptyValue($this->Gambar->Upload->FileName)) {
							$newFiles = array($this->Gambar->Upload->FileName);
							$newFiles2 = array($rsnew['Gambar']);
							$newFileCount = count($newFiles);
							for ($i = 0; $i < $newFileCount; $i++) {
								if ($newFiles[$i] <> "") {
									$file = UploadTempPath($this->Gambar, $this->Gambar->Upload->Index) . $newFiles[$i];
									if (file_exists($file)) {
										if (@$newFiles2[$i] <> "") // Use correct file name
											$newFiles[$i] = $newFiles2[$i];
										if (!$this->Gambar->Upload->saveToFile($newFiles[$i], TRUE, $i)) { // Just replace
											$this->setFailureMessage($Language->phrase("UploadErrMsg7"));
											return FALSE;
										}
									}
								}
							}
						} else {
							$newFiles = array();
						}
						if (DELETE_UPLOADED_FILES) {
							foreach ($oldFiles as $oldFile) {
								if ($oldFile <> "" && !in_array($oldFile, $newFiles))
									@unlink($this->Gambar->oldPhysicalUploadPath() . $oldFile);
							}
						}
					}
				}
			} else {
				if ($this->getSuccessMessage() <> "" || $this->getFailureMessage() <> "") {

					// Use the message, do nothing
				} elseif ($this->CancelMessage <> "") {
					$this->setFailureMessage($this->CancelMessage);
					$this->CancelMessage = "";
				} else {
					$this->setFailureMessage($Language->phrase("UpdateCancelled"));
				}
				$editRow = FALSE;
			}
		}

		// Call Row_Updated event
		if ($editRow)
			$this->Row_Updated($rsold, $rsnew);
		$rs->close();

		// Gambar
		if ($this->Gambar->Upload->FileToken <> "")
			CleanUploadTempPath($this->Gambar->Upload->FileToken, $this->Gambar->Upload->Index);
		else
			CleanUploadTempPath($this->Gambar, $this->Gambar->Upload->Index);

		// Write JSON for API request
		if (IsApi() && $editRow) {
			$row = $this->getRecordsFromRecordset([$rsnew], TRUE);
			WriteJson(["success" => TRUE, $this->TableVar => $row]);
		}
		return $editRow;
	}

